def calc_elixhauser_comorbidity(df):
    comorbid_score = 0
    seen_comorbidities = set()

    diagnoses = df.filter(like='I10_DX').values.flatten()
    diagnoses = [str(code) for code in diagnoses if pd.notna(code)]

    for icd10, description in elixhauser_mapping.items():
        if any(dx.startswith(icd10) for dx in diagnoses) and description not in seen_comorbidities:
            comorbid_score += comorbid_weights[description]
            seen_comorbidities.add(description)
    
    return comorbid_score
